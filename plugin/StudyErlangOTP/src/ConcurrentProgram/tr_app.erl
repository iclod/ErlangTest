%% ====================================================================
%% API functions
%% ====================================================================
start(_Type, _StartArgs) ->
	case tr_sup:start_link() of
		{ok, Pid} ->
			{ok, Pid};
		Other ->
			{error, Other}
	end.

stop(_State) ->
	ok.


